module Usuarios.ts {
	
	export class CambiarClave implements IControlador {
		
		//---  Propiedades de Formulario --- //
		public form: JQuery;
		public datatables: DataTables.DataTable[];
		
		 
		public ClaveActual:JQuery; 
		public ClaveNueva:JQuery; 
		public ReClaveNueva:JQuery; 
		public Cancelar: JQuery;
		public Guardar: JQuery;
		//---  /Propiedades de Formulario --- //
		
		constructor(hash: string){
			this.form = $("#" + hash);
			this.datatables = [];
			
		 
            this.ClaveActual = $("#ClaveActual"+hash); 
            this.ClaveNueva = $("#ClaveNueva"+hash); 
            this.ReClaveNueva = $("#ReClaveNueva"+hash); 
			//operaciones
			this.Cancelar = $("#Cancelar"+hash);
			this.Guardar = $("#Guardar"+hash);
			
			//Establece el foco
			this.ClaveActual.focus();
		}
		
		public limpiar():void {
			
            this.ClaveActual.val("");
            this.ClaveNueva.val("");
            this.ReClaveNueva.val("");
		}
		
		//--- Funciones para seteo de Datatables ---/
		//--- /Funciones para seteo de Datatables ---/
		
		public fnRegistrarDataTable(d: DataTables.DataTable): void {
			this.datatables.push(d);
		}

		public fnRefrescarDataTables():void {
			var that = this;
			this.datatables.forEach(function (d) {
				d.fnDraw();
			});    
		}
	} //JS
} // module
